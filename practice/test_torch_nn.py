# 打印优化器的状态字典
print("Optimizer's state_dict:")
for var_name in optimizer.state_dict():
    print(var_name, "\t", optimizer.state_dict()[var_name])
input = torch.randn(6, 3, 5,5) 
print(input)
out = model(input)
model.zero_grad()
